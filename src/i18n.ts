const catalogues: Record<Language, Record<TranslationKey, string>> = { en, de }

type TParams = Record<string, string | number>

function interpolate(template: string, params?: TParams): string {
  if (!params) return template
  return template.replace(/\{(\w+)\}/g, (m, key) =>
    key in params ? String(params[key]) : m,
  )
}

// Standalone translate — for use outside React (rare). Prefer useT() in
// components so they re-render on a language change.
export function translate(lang: Language, key: TranslationKey, params?: TParams): string {
  const str = catalogues[lang]?.[key] ?? en[key] ?? key
  return interpolate(str, params)
}

export type TFunction = (key: TranslationKey, params?: TParams) => string

// React hook: returns a `t()` bound to the store's current language. Components
// re-render whenever the language changes.
export function useT(): TFunction {
  const lang = useWorkspaceStore(s => s.language)
  // Stable per-language so it's safe to use in effect/memo dependency arrays.
  return useMemo<TFunction>(() => (key, params) => translate(lang, key, params), [lang])
}
