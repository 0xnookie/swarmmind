// ── Internationalisation (i18n) ─────────────────────────────────────────────
// A tiny, dependency-free i18n layer. The active language lives in the Zustand
// store (`language`, persisted as the `language` app setting); the `useT()` hook
// subscribes to it so every consuming component re-renders on a language switch.
//
// `en` is the source of truth: its keys define the catalogue. `de` is typed as
// `Record<TranslationKey, string>`, so `npm run typecheck` fails if a German
// string is ever missing — keeping the two catalogues in lockstep. Lookups fall
// back to English at runtime as a final safety net.
//
// Interpolation: `t('key', { name: 'x' })` replaces `{name}` placeholders.

